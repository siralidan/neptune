  <script>
    // Counter function for transactions every 24 hours
    function countTransactions() {
        let currentCount = 0;
        const targetCount = 44; // Target count for transactions
        const increment = 1; // Increment amount for each step
        const speed = 150; // Speed of counting in milliseconds

        const counterElement = document.getElementById('transactions-counter');

        const timer = setInterval(() => {
            currentCount += increment;
            counterElement.textContent = currentCount + ' million';
            if (currentCount >= targetCount) {
                clearInterval(timer);
                counterElement.textContent = targetCount + ' million';
            }
        }, speed);
    }

    // Counter function for assets under holding
    function countAssets() {
        let currentCount = 0;
        const targetCount = 119; // Target count for assets
        const increment = 1; // Increment amount for each step
        const speed = 100; // Speed of counting in milliseconds

        const counterElement = document.getElementById('assets-counter');

        const timer = setInterval(() => {
            currentCount += increment;
            counterElement.textContent = '$' + currentCount + ' trillion';
            if (currentCount >= targetCount) {
                clearInterval(timer);
                counterElement.textContent = '$' + targetCount + ' trillion';
            }
        }, speed);
    }

    // Counter function for active members annually
    function countMembers() {
        let currentCount = 0;
        const targetCount = 46000; // Target count for members
        const increment = 1000; // Increment amount for each step
        const speed = 200; // Speed of counting in milliseconds

        const counterElement = document.getElementById('members-counter');

        const timer = setInterval(() => {
            currentCount += increment;
            counterElement.textContent = currentCount.toLocaleString() + ' users';
            if (currentCount >= targetCount) {
                clearInterval(timer);
                counterElement.textContent = targetCount.toLocaleString() + ' users';
            }
        }, speed);
    }

    // Call the counter functions when the page is loaded
    window.addEventListener('load', () => {
        countTransactions();
        countAssets();
        countMembers();
    });
</script>
